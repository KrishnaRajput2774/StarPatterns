public class Pattern10 {
	
	public static void main(String[] args) {
		
		
		for(int r=1;r<=5;r++)
		{
			for(int c=1;c<=3;c++)
			{
				System.out.print(c+" "+r+" ");
			}
			System.out.println();
		}
		
	}

}
